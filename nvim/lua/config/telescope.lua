local builtin = require("telescope.builtin")
vim.keymap.set("n", "<C-p>", builtin.find_files, { desc = "fuzzy find files in current directory" })
vim.keymap.set("n", "<leader>fg", builtin.live_grep, { desc = "live grep in current directory" })
vim.keymap.set("n", "<leader>fb", builtin.buffers, { desc = "fuzzy find buffers" })
vim.keymap.set("n", "<leader>fh", builtin.help_tags, { desc = "list help tags" })
vim.keymap.set("n", "<leader>fr", builtin.registers, { desc = "list registers; paste content on <cr>" })
vim.keymap.set("n", "<leader>fc", builtin.current_buffer_fuzzy_find, { desc = "fuzzy find in current buffer" })
vim.keymap.set("n", "<leader>fk", builtin.keymaps, { desc = "list normal mode keymaps" })
vim.keymap.set("n", "<leader>fs", builtin.spell_suggest, { desc = "list spelling suggestions for word at cursor; replace on <cr>" })
vim.keymap.set("n", "<leader>ft", builtin.treesitter, { desc = "list function names and variables" })
